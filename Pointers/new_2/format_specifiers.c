#include<stdio.h>


int main()
{
	int a = 66;
	printf("unsigned char = %p", (unsigned char)a);
	printf("\n");
	printf("signed char = %p", (signed char)a);
	printf("\n");
	printf("unsigned int = %p", (unsigned int)a);
	printf("\n");
	printf("unsigned short int = %p", (unsigned short int)a);
	printf("\n");
	printf("int = %p", (int)a);
	printf("\n");
	printf("short int = %p", (short int)a);
	printf("\n");
	return 0;
}
